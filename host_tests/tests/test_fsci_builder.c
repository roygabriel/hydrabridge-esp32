static void rebuild_and_compare(const uint8_t *captured, size_t captured_len, uint16_t msg_id)
{
    TEST_ASSERT_EQUAL_UINT8(0x02, captured[0]);
    uint16_t plen = (uint16_t)captured[7] | ((uint16_t)captured[8] << 8);
    TEST_ASSERT_EQUAL_size_t(captured_len, FSCI_FRAME_OVERHEAD_BYTES + plen);

    uint8_t built[128];
    size_t written = 0;
    int rc = fsci_build(captured[1], captured[2], msg_id,
                        &captured[FSCI_HEADER_LEN_BYTES], plen,
                        built, sizeof built, &written);
    TEST_ASSERT_EQUAL_INT(0, rc);
    TEST_ASSERT_EQUAL_size_t(captured_len, written);
    TEST_ASSERT_EQUAL_HEX8_ARRAY(captured, built, captured_len);
}

static void test_build_lights_on(void)  { rebuild_and_compare(TX_ON,   sizeof TX_ON,   0x0050); }
static void test_build_lights_off(void) { rebuild_and_compare(TX_OFF,  sizeof TX_OFF,  0x0051); }
static void test_build_lbm(void)        { rebuild_and_compare(TX_LBM,  sizeof TX_LBM,  0x0053); }
static void test_build_moonlight(void)  { rebuild_and_compare(TX_MOON, sizeof TX_MOON, 0x0054); }

/* Edge cases. */

static void test_build_zero_payload(void)
{
    uint8_t out[16] = {0};
    size_t written = 0;
    int rc = fsci_build(0xde, 0x18, 0x1234, NULL, 0, out, sizeof out, &written);
    TEST_ASSERT_EQUAL_INT(0, rc);
    TEST_ASSERT_EQUAL_size_t(FSCI_FRAME_OVERHEAD_BYTES, written);
    TEST_ASSERT_EQUAL_HEX8(0x02, out[0]);
    TEST_ASSERT_EQUAL_HEX8(0xde, out[1]);
    TEST_ASSERT_EQUAL_HEX8(0x18, out[2]);
    TEST_ASSERT_EQUAL_HEX8(0x34, out[3]); /* msg_id LE low */
    TEST_ASSERT_EQUAL_HEX8(0x12, out[4]); /* msg_id LE high */
    TEST_ASSERT_EQUAL_HEX8(0x00, out[7]); /* payload_len LE low */
    TEST_ASSERT_EQUAL_HEX8(0x00, out[8]); /* payload_len LE high */
    uint16_t expected = fsci_crc16(&out[1], 8);
    TEST_ASSERT_EQUAL_HEX8((uint8_t)(expected & 0xFF),       out[9]);
    TEST_ASSERT_EQUAL_HEX8((uint8_t)((expected >> 8) & 0xFF), out[10]);
}

static void test_build_buffer_too_small(void)
{
    uint8_t out[10];
    size_t written = 0;
    int rc = fsci_build(0xde, 0x18, 0, NULL, 0, out, sizeof out, &written);
    TEST_ASSERT_EQUAL_INT(-1, rc);
}

static void test_build_null_out(void)
{
    size_t written = 0;
    int rc = fsci_build(0xde, 0x18, 0, NULL, 0, NULL, 100, &written);
    TEST_ASSERT_EQUAL_INT(-1, rc);
}

void register_fsci_builder_tests(void)
{
    RUN_TEST(test_build_lights_on);
    RUN_TEST(test_build_lights_off);
    RUN_TEST(test_build_lbm);
    RUN_TEST(test_build_moonlight);
    RUN_TEST(test_build_zero_payload);
    RUN_TEST(test_build_buffer_too_small);
    RUN_TEST(test_build_null_out);
}
