/* Phase 1.1: CRC-16/CCITT-FALSE golden vectors lifted from
 * docs/myai-ble-reverse-engineering.md (lines 1012-1142). Each frame
 * is the exact wire payload captured during live hardware testing.
 * CRC is computed over frame bytes [1 .. last-payload-byte] and
 * stored little-endian after the payload. */
#include "unity.h"
#include "all_tests.h"
#include "fsci_codec.h"

#include <stddef.h>
#include <stdint.h>

static void check_frame(const uint8_t *frame, size_t frame_len, uint16_t expected_crc)
{
    /* CRC scope: skip start marker at offset 0, skip the 2-byte CRC at
     * the end. */
    TEST_ASSERT_TRUE(frame_len > 3);
    TEST_ASSERT_EQUAL_UINT8(FSCI_START_MARKER, frame[0]);

    uint16_t computed = fsci_crc16(&frame[1], frame_len - 3);
    TEST_ASSERT_EQUAL_HEX16(expected_crc, computed);

    /* Stored bytes match LE encoding of expected_crc. */
    uint8_t stored_lo = frame[frame_len - 2];
    uint8_t stored_hi = frame[frame_len - 1];
    TEST_ASSERT_EQUAL_HEX8((uint8_t)(expected_crc & 0xFF),       stored_lo);
    TEST_ASSERT_EQUAL_HEX8((uint8_t)((expected_crc >> 8) & 0xFF), stored_hi);
}

