/* Property: CRC of empty input is the init value (0xFFFF). */
static void test_crc_empty(void)
{
    TEST_ASSERT_EQUAL_HEX16(0xFFFF, fsci_crc16((const uint8_t *)"", 0));
}

/* Property: CRC of NULL+len=0 returns init. */
static void test_crc_null_safe(void)
{
    TEST_ASSERT_EQUAL_HEX16(0xFFFF, fsci_crc16(NULL, 0));
    TEST_ASSERT_EQUAL_HEX16(0xFFFF, fsci_crc16(NULL, 999));
}

